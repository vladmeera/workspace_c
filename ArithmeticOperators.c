#include <stdio.h>

int main() {

    // arithmetic operators

    // + addition
    // - subtraction
    // * multiplication
    // / division
    // % modulus
    // ++ increment
    // -- decrement

    int a = 10;
    int b = 3;
    float c = 3;

    printf("a + b = %d\n", a + b);
    printf("a - b = %d\n", a - b);
    printf("a * b = %d\n", a * b);
    printf("a / b = %.1f\n", a / (float) b);
    printf("a %% b = %d\n", a % b);
    printf("a++ = %d\n", a++);
    printf("++a = %d\n", ++a);
    printf("b-- = %d\n", b--);
    printf("--b = %d\n", --b);

    return 0;

}